package com.proquest.mtg.dismetadataservice.metadata;

public class DisGeneralMapping {
	
	private final String degreevalue1;
	private final String degreeValue2;
	
	public DisGeneralMapping(String degreevalue1, String degreevalue2) {
		this.degreevalue1 = degreevalue1;
		this.degreeValue2 = degreevalue2;
	}

	public String getDegreevalue1() {
		return degreevalue1;
	}

	public String getDegreeValue2() {
		return degreeValue2;
	}

}
